import turtle

bob = turtle.Turtle()

def draw_square(size, color):
    bob.color(color)
    for _ in range(4):
        bob.forward(size)
        bob.left(90)

draw_square(50, "purple")
bob.left(90)
draw_square(75, "red")
bob.left(90)
